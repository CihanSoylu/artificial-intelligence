def naked_twins(values):
    """Eliminate values using the naked twins strategy.

    The naked twins strategy says that if you have two or more unallocated boxes
    in a unit and there are only two digits that can go in those two boxes, then
    those two digits can be eliminated from the possible assignments of all other
    boxes in the same unit.

    Parameters
    ----------
    values(dict)
        a dictionary of the form {'box_name': '123456789', ...}

    Returns
    -------
    dict
        The values dictionary with the naked twins eliminated from peers

    Notes
    -----
    Your solution can either process all pairs of naked twins from the input once,
    or it can continue processing pairs of naked twins until there are no such
    pairs remaining -- the project assistant test suite will accept either
    convention. However, it will not accept code that does not process all pairs
    of naked twins from the original input. (For example, if you start processing
    pairs of twins and eliminate another pair of twins before the second pair
    is processed then your code will fail the PA test suite.)

    The first convention is preferred for consistency with the other strategies,
    and because it is simpler (since the reduce_puzzle function already calls this
    strategy repeatedly).
    """
    # TODO: Implement this function!
    
    #List of boxes with exactly two possible values
    boxes_of_length_two = []
    
    #List of tuples (box_1, box_2) where box_1 and box_2 have the same two possible values and they are peers of each other
    twins = []
    
    #Determine all boxes with exactly two possible values
    for box in boxes:
        if len(values[box]) == 2:
            boxes_of_length_two.append(box)
    
    #Find the twins
    if len(boxes_of_length_two) == 0:
        return values
    
    for box in boxes_of_length_two:
        #First find the boxes of length two that are peers of the box. 
        peers_of_length_two = list(set(peers[box]).intersection(set(boxes_of_length_two)))
        if len(peers_of_length_two) > 0:
            # If there are peers of length two, check if they are actual twins.
            for peer in peers_of_length_two:
                if values[peer] == values[box]:
                    twins.append((box, peer))
    
    #For each twin remove the twin_values from the other boxes in the units that contains the twins. 
    for twin in twins:
        for unit in units[twin[0]]:
            # If the unit contains both twins
            if twin[1] in unit: 
                for box in unit:
                    if box not in twin: #Don't remove the values from the twins.
                        for v in values[twin[0]]:
                            values[box] = values[box].replace(v, '')   
            
    return values
    

